def t(x, y):
    if x == y:
        return (x % 2) + (x // 2) * 4
    elif x == y+2:
        return x + ((x // 2) - 1) * 2
    else:
        return -1


n = int(input())
coords = []
for i in range(n):
    coords.append(list(map(int, input().split(" "))))

for c in coords:
    print(t(c[0], c[1]))
