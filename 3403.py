
x = int(input())
y = int(input())
z = int(input())
d = int(input())
sum_value = x+y+z+d
avg = sum_value/4
product= x*y*z*d
max_value = max(x,y,z,d)
min_value = min(x,y,z,d)
print(f'Sum : {sum_value:.6f}')
print(f'Average : {avg:.6f}')
print(f'Product : {product:.6f}')
print(f'MAX : {max_value:.6f}')
print(f'MIN : {min_value:.6f}')


